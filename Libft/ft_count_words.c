#include "libft.h"

int		ft_count_words(char const *s, char c)
{
	int	words;
	int	i;

	words = 0;
	i = 0;
	while (s[i] != '\0')
	{
		while (s[i] != '\0' && s[i] == c)
			i++;
		if (s[i] == '\0')
			return (words);
		while (s[i] != '\0' && s[i] != c)
			i++;
		words++;
	}
	return (words);
}
