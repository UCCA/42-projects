#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*ptr;

	i = 0;
	ptr = (char *)b;
	while (len > 0)
	{
		ptr[i] = c;
		i++;
		len--;
	}
	return (ptr);
}
