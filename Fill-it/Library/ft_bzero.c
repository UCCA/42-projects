#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*ptr;

	i = 0;
	ptr = (char *)s;
	while (n > 0)
	{
		ptr[i] = '\0';
		i++;
		n--;
	}
}
