#include "libft.h"

int	ft_count_case(int n)
{
	int		c;

	c = 0;
	if (n < 0)
	{
		n = -n;
		c++;
	}
	while (n > 0)
	{
		n = n / 10;
		c++;
	}
	return (c);
}
